function applyStylesToSelector(styles, selector) {
    var elstyle = '';
    selector = selector.replace(/@/g, ' ').replace(/!/g, '.');
    Object.keys(styles).forEach(function(key){
        if (styles[key] == '' || styles[key] == 'default') return;
        if (typeof styles[key] === "object") { applyStylesToSelector(styles[key], selector); return; }
        var cssKey = key.replace(/_/g, '-');
        if (!isNaN(parseFloat(styles[key])) && !styles[key].toString().endsWith('px')) styles[key] = styles[key] + 'px';
        elstyle = elstyle + cssKey + ':' + styles[key] + ' !important; ';
    });
    var currentStyle = $(selector).attr('style') || '';
    if (selector.includes('.')) $(selector).eq(0).attr('style', currentStyle + ';' + elstyle);
    else $(selector).attr('style', currentStyle + ';' + elstyle);
}
